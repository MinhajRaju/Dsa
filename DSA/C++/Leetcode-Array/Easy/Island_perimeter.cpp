#include <iostream>
#include <vector>

void islandPreimeter(std::vector<std::vector<int>>& grid){
    int rows = grid.size();
    int cols = grid[0].size();  
    int perimiter = 0;

    for (int i = 0 ; i < rows; i++){

        for (int j = 0; j < cols; j++ ){

            if(grid[i][j] != 0){
                perimiter +=4;
                if( i > 0 && grid[i-1][j] == 1) perimiter -=2;
                if( j > 0 && grid[i][j-1] == 1) perimiter  -= 2; 
            }
        }
   
    }

    std::cout << perimiter;

}
